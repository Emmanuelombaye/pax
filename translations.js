class I18n {
  constructor() {
    this.currentLang = localStorage.getItem('pax-lang') || 'en';
    this.init();
  }

  init() {
    this.buildSwitcher();
    this.applyLanguage(this.currentLang, false);
    this.bindEvents();
  }

  // Build the language switcher dropdown
  buildSwitcher() {
    const switcher = document.getElementById('lang-switcher');
    if (!switcher) return;

    const current = translations[this.currentLang];

    // Current language button
    const btn = switcher.querySelector('.lang-current');
    btn.innerHTML = `<span class="lang-flag">${current.flag}</span><span class="lang-code">${this.currentLang.toUpperCase()}</span><span class="lang-chevron">▾</span>`;

    // Dropdown items
    const dropdown = switcher.querySelector('.lang-dropdown');
    dropdown.innerHTML = '';

    Object.keys(translations).forEach(code => {
      const t = translations[code];
      const item = document.createElement('button');
      item.className = `lang-option${code === this.currentLang ? ' active' : ''}`;
      item.dataset.lang = code;
      item.innerHTML = `<span class="lang-flag">${t.flag}</span><span class="lang-option-label">${t.label}</span><span class="lang-option-code">${code.toUpperCase()}</span>`;
      dropdown.appendChild(item);
    });
  }

  // Apply a language to the entire page
  applyLanguage(lang, animate = true) {
    const t = translations[lang];
    if (!t) return;

    this.currentLang = lang;
    localStorage.setItem('pax-lang', lang);

    // Set direction (RTL for Arabic)
    document.documentElement.setAttribute('dir', t.dir);
    document.documentElement.setAttribute('lang', t.lang);

    // Optionally add a transition class for smooth text swaps
    if (animate) {
      document.body.classList.add('lang-transitioning');
      setTimeout(() => document.body.classList.remove('lang-transitioning'), 400);
    }

    // Swap all translatable text
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      if (t[key] !== undefined) {
        if (el.dataset.i18nHtml !== undefined) {
          el.innerHTML = t[key];
        } else {
          el.textContent = t[key];
        }
      }
    });

    // Update switcher button
    const btn = document.querySelector('.lang-current');
    if (btn) {
      btn.innerHTML = `<span class="lang-flag">${t.flag}</span><span class="lang-code">${lang.toUpperCase()}</span><span class="lang-chevron">▾</span>`;
    }

    // Update active state in dropdown
    document.querySelectorAll('.lang-option').forEach(opt => {
      opt.classList.toggle('active', opt.dataset.lang === lang);
    });
  }

  bindEvents() {
    const switcher = document.getElementById('lang-switcher');
    if (!switcher) return;

    const btn = switcher.querySelector('.lang-current');
    const dropdown = switcher.querySelector('.lang-dropdown');

    // Toggle dropdown
    btn.addEventListener('click', (e) => {
      e.stopPropagation();
      switcher.classList.toggle('open');
    });

    // Select language
    dropdown.addEventListener('click', (e) => {
      const option = e.target.closest('.lang-option');
      if (!option) return;
      const lang = option.dataset.lang;
      this.applyLanguage(lang);
      switcher.classList.remove('open');
    });

    // Close on outside click
    document.addEventListener('click', () => {
      switcher.classList.remove('open');
    });

    // Prevent closing when clicking inside
    switcher.addEventListener('click', (e) => {
      e.stopPropagation();
    });
  }
}

